package filesync

import "testing"

func TestIsIncompressible(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name string
		head []byte
		want bool
	}{
		{"zst", []byte{0x28, 0xb5, 0x2f, 0xfd, 0x00, 0x00}, true},
		{"gz", []byte{0x1f, 0x8b, 0x08, 0x00}, true},
		{"xz", []byte{0xfd, 0x37, 0x7a, 0x58, 0x5a, 0x00}, true},
		{"bz2", []byte{0x42, 0x5a, 0x68, 0x39}, true},
		{"lz4", []byte{0x04, 0x22, 0x4d, 0x18}, true},
		{"jpg", []byte{0xff, 0xd8, 0xff, 0xe0}, true},
		{"png", []byte{0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a}, true},
		{"gif", []byte{0x47, 0x49, 0x46, 0x38, 0x39, 0x61}, true},
		{"mp3 id3", []byte{0x49, 0x44, 0x33, 0x03}, true},
		{"mp3 frame", []byte{0xff, 0xfb, 0x90, 0x04}, true},
		{"webm", []byte{0x1a, 0x45, 0xdf, 0xa3, 0x01}, true},
		{"zip", []byte{0x50, 0x4b, 0x03, 0x04, 0x14}, true},
		{"7z", []byte{0x37, 0x7a, 0xbc, 0xaf, 0x27, 0x1c}, true},
		{"pdf", []byte{0x25, 0x50, 0x44, 0x46, 0x2d, 0x31}, true},
		{"rar", []byte{0x52, 0x61, 0x72, 0x21, 0x1a, 0x07, 0x00}, true},
		{"mp4 ftyp box", append(make([]byte, 4), []byte("ftypisom\x00\x00\x00\x00")...), true},
		{"plain text", []byte("hello world\n"), false},
		{"json", []byte(`{"key":"value"}`), false},
		{"xml", []byte(`<?xml version="1.0"?>`), false},
		{"binary not-compressed", []byte{0x7f, 0x45, 0x4c, 0x46, 0x02}, false}, // ELF
		{"empty", nil, false},
		{"too short", []byte{0x28, 0xb5}, false},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := isIncompressible(tc.head)
			if got != tc.want {
				t.Fatalf("isIncompressible(%x) = %v, want %v", tc.head, got, tc.want)
			}
		})
	}
}
